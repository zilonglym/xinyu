package com.graby.store.portal.qm.enums;

/**
 * xml转化类型的枚举
 * @author lenovo
 *
 */
public enum XmlEnum {
	REQUEST("request","请求"),
	RESPONSE("response","返回")
	;
	public String getKey() {
		return key;
	}
	public String getValue() {
		return value;
	}
	private String key;
	private String value;
	private XmlEnum(String key,String value){
		this.key=key;
		this.value=value;
	}
	

}
