package com.xinyu.model.base;

import com.xinyu.model.common.Entity;

/**
 * 仓库实体
 * 
 * @author yangmin 2017年5月25日
 *
 */
public class Centro extends Entity {
	/**
	 * 仓库名称
	 */
	private String name;
	/**
	 * 联系人
	 */
	private String person;
	/**
	 * 移动电话
	 */
	private String mobile;
	/**
	 * 电话
	 */
	private String phone;
	/**
	 * 地址
	 */
	private String address;
	/**
	 * 奇门仓库编码
	 */
	private String code;
	/**
	 * 菜鸟仓储编 码
	 */
	private String storeCode;
	/**
	 * 顺丰地址
	 */
	private String sfAddress;
	/**
	 * 备注
	 */
	private String remark;
	/**
	 * 省
	 */
	private String province;
	/**
	 * 市
	 */
	private String city;
	/**
	 * 区
	 */
	private String area;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPerson() {
		return person;
	}

	public void setPerson(String person) {
		this.person = person;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getSfAddress() {
		return sfAddress;
	}

	public void setSfAddress(String sfAddress) {
		this.sfAddress = sfAddress;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getStoreCode() {
		return storeCode;
	}

	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}
}
