package com.xinyu.model.base;

import com.xinyu.model.common.Entity;


/**
 * @author shark_cj
 * @since 2017-05-03
 * 包裹商品信息
 */
public class PackageItem  extends  Entity{

	private  PackageInfo packageItem;
	
	/**
	 * true  商品ID
	 * itemId
	 */
	private Item  Item;
	
	/**
	 * TRUE   商品编码
	 * itemCode
	 */
	private String  itemCode;
	
	/**
	 * TRUE   此包裹里该商品的数量
	 * quantity
	 */
	private int itemQuantity;

	public PackageInfo getPackageItem() {
		return packageItem;
	}

	public void setPackageItem(PackageInfo packageItem) {
		this.packageItem = packageItem;
	}

	public Item getItem() {
		return Item;
	}

	public void setItem(Item item) {
		Item = item;
	}

	public String getItemCode() {
		return itemCode;
	}

	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}

	public int getItemQuantity() {
		return itemQuantity;
	}

	public void setItemQuantity(int itemQuantity) {
		this.itemQuantity = itemQuantity;
	}
}
