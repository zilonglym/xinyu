package com.xinyu.model.inventory;

import java.util.Date;

import com.xinyu.model.base.Item;
import com.xinyu.model.base.User;
import com.xinyu.model.common.Entity;
import com.xinyu.model.inventory.enums.InventoryOrderTypeEnum;
import com.xinyu.model.inventory.enums.InventoryTypeEnum;

/**
 * @author shark_cj
 * @since   2017-05-02    
 * 库存流水记录表
 */
public class InventoryRecord  extends  Entity{
	
	private Item  item;
	
	private String  orderNo;
	
	private User  user;
	
	private Date createDate;
	
	private long  num;
	
	private InventoryOrderTypeEnum  orderType ;
	
	private  InventoryTypeEnum  inventoryType;
	
	
	private String  description;
	

	public Item getItem() {
		return item;
	}

	public void setItem(Item item) {
		this.item = item;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public long getNum() {
		return num;
	}

	public void setNum(long num) {
		this.num = num;
	}

	public InventoryOrderTypeEnum getOrderType() {
		return orderType;
	}

	public void setOrderType(InventoryOrderTypeEnum orderType) {
		this.orderType = orderType;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public InventoryTypeEnum getInventoryType() {
		return inventoryType;
	}

	public void setInventoryType(InventoryTypeEnum inventoryType) {
		this.inventoryType = inventoryType;
	}
	
}
