package com.xinyu.model.trade;

import com.xinyu.model.base.Item;
import com.xinyu.model.common.Entity;

/**
 * 退货返回明细
 * @author yangmin
 * 2017年12月28日
 *
 * 这里操作界面的时候，需要弹出一个商品选择框来让操作者选择商品。选择完再填数字。
 * 一个退回包裹里面包括赠品会有多个商品，每一个都需要保存。
 * 
 */
public class ShipOrderBackItem extends Entity{

	private ShipOrderBack orderBack;
	
	private Item item;
	
	private String itemName;
	
	private int quantity;

	public ShipOrderBack getOrderBack() {
		return orderBack;
	}

	public void setOrderBack(ShipOrderBack orderBack) {
		this.orderBack = orderBack;
	}

	public Item getItem() {
		return item;
	}

	public void setItem(Item item) {
		this.item = item;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	
	
	
}
